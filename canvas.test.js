const CreateCanvas = require("./canvas");

const firstInstance = new CreateCanvas(10, 5);
const secondInstance = new CreateCanvas(10, 10);
const thirdInstance = new CreateCanvas(12, 8);

test('Test first canvas', () => {
    expect(firstInstance.getCanvas()).toEqual(firstCanvas[0])
    expect(firstInstance.drawCanvas(3, 2, 6, 2)).toEqual(firstCanvas[1])
    expect(firstInstance.drawRectangle(7, 3, 10, 5)).toEqual(firstCanvas[2])
    expect(firstInstance.bucketFill(6, 3, '@')).toEqual(firstCanvas[3])
})

test('Test second canvas', () => {
    expect(secondInstance.getCanvas()).toEqual(secondCanvas[0])
    expect(secondInstance.drawCanvas(7, 2, 7, 7)).toEqual(secondCanvas[1])
    expect(secondInstance.drawRectangle(1, 8, 4, 10)).toEqual(secondCanvas[2])
    expect(secondInstance.bucketFill(1, 1, '#')).toEqual(secondCanvas[3])
})


test('Test third canvas', () => {
    expect(thirdInstance.getCanvas()).toEqual(thirdCanvas[0])
    expect(thirdInstance.drawCanvas(1, 2, 1, 8)).toEqual(thirdCanvas[1])
    expect(thirdInstance.drawRectangle(6, 3, 8, 7)).toEqual(thirdCanvas[2])
    expect(thirdInstance.bucketFill(12, 8, '%')).toEqual(thirdCanvas[3])
})

